CREATE TABLE CIUDADES (
    CiudadID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1000,
    Nombre VARCHAR2(80) NOT NULL,
    Pais VARCHAR2(80) NOT NULL,
    CONSTRAINT PK_CIUDADES PRIMARY KEY (CiudadID),
    CONSTRAINT UK_CIUDADES_NOMBRE_PAIS UNIQUE (Nombre, Pais)
);

CREATE TABLE USUARIOS (
    UsuarioID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1000,
    Nombre VARCHAR2(100) NOT NULL,
    Apellido VARCHAR2(100) NOT NULL,
    DocumentoIdentidad VARCHAR2(30) NOT NULL,
    CONSTRAINT PK_USUARIOS PRIMARY KEY (UsuarioID),
    CONSTRAINT UK_USUARIOS_DOCUMENTO UNIQUE (DocumentoIdentidad)
);

CREATE TABLE HOTELES (
    HotelID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1000,
    Nombre VARCHAR2(120) NOT NULL,
    Ciudad NUMBER NOT NULL,
    Direccion VARCHAR2(200) NOT NULL,
    Telefono VARCHAR2(30) NOT NULL,
    DescripcionGeneral VARCHAR2(500),
    CONSTRAINT PK_HOTELES PRIMARY KEY (HotelID),
    CONSTRAINT FK_HOTELES_CIUDADES FOREIGN KEY (Ciudad)
        REFERENCES CIUDADES (CiudadID),
    CONSTRAINT UK_HOTELES_NOMBRE_CIUDAD UNIQUE (Nombre, Ciudad)
);

CREATE TABLE CLIENTES (
    UsuarioID NUMBER NOT NULL,
    Correo VARCHAR2(150) NOT NULL,
    Telefono VARCHAR2(30) NOT NULL,
    CONSTRAINT PK_CLIENTES PRIMARY KEY (UsuarioID),
    CONSTRAINT FK_CLIENTES_USUARIOS FOREIGN KEY (UsuarioID)
        REFERENCES USUARIOS (UsuarioID)
        ON DELETE CASCADE,
    CONSTRAINT UK_CLIENTES_CORREO UNIQUE (Correo)
);

CREATE TABLE ADMINS (
    UsuarioID NUMBER NOT NULL,
    CONSTRAINT PK_ADMINS PRIMARY KEY (UsuarioID),
    CONSTRAINT FK_ADMINS_USUARIOS FOREIGN KEY (UsuarioID)
        REFERENCES USUARIOS (UsuarioID)
        ON DELETE CASCADE
);

CREATE TABLE SERVICIOSADICIONALES (
    ServicioID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1000,
    Nombre VARCHAR2(120) NOT NULL,
    Descripcion VARCHAR2(300),
    TipoCosto VARCHAR2(30) NOT NULL,
    Precio NUMBER(12,2) NOT NULL,
    CONSTRAINT PK_SERVICIOSADICIONALES PRIMARY KEY (ServicioID),
    CONSTRAINT UK_SERVICIOSADICIONALES_NOMBRE UNIQUE (Nombre),
    CONSTRAINT CK_SERVAD_TIPO_COSTO CHECK (TipoCosto IN ('Por uso', 'Costo Fijo')),
    CONSTRAINT CK_SERVAD_PRECIO CHECK (Precio >= 0)
);

CREATE TABLE HABITACIONES (
    HabitacionID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1000,
    Tipo VARCHAR2(80) NOT NULL,
    CostoNocheAlta NUMBER(12,2) NOT NULL,
    CostoNocheBaja NUMBER(12,2) NOT NULL,
    Dimensiones NUMBER(7,2),
    TipoVista VARCHAR2(40),
    HotelID NUMBER NOT NULL,
    Disponible NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT PK_HABITACIONES PRIMARY KEY (HabitacionID),
    CONSTRAINT FK_HABITACIONES_HOTELES FOREIGN KEY (HotelID)
        REFERENCES HOTELES (HotelID),
    CONSTRAINT UK_HABITACIONES_HOTEL_TIPO UNIQUE (HotelID, Tipo),
    CONSTRAINT CK_HAB_COSTOS CHECK (CostoNocheAlta >= 0 AND CostoNocheBaja >= 0),
    CONSTRAINT CK_HAB_COSTO_ORDEN CHECK (CostoNocheAlta >= CostoNocheBaja),
    CONSTRAINT CK_HAB_DIMENSIONES CHECK (Dimensiones IS NULL OR Dimensiones > 0),
    CONSTRAINT CK_HAB_DISPONIBLE CHECK (Disponible IN (0, 1)),
    CONSTRAINT CK_HAB_VISTA CHECK (
        TipoVista IS NULL OR TipoVista IN (
            'Ciudad', 'Interior', 'Mar', 'Monumentos', 'Piscina',
            'Estandar', 'Estándar'
        )
    )
);

CREATE TABLE CAMAS (
    Habitacion NUMBER NOT NULL,
    Tipo VARCHAR2(30) NOT NULL,
    Cantidad NUMBER(3) NOT NULL,
    CONSTRAINT PK_CAMAS PRIMARY KEY (Habitacion, Tipo),
    CONSTRAINT FK_CAMAS_HABITACIONES FOREIGN KEY (Habitacion)
        REFERENCES HABITACIONES (HabitacionID)
        ON DELETE CASCADE,
    CONSTRAINT CK_CAMAS_TIPO CHECK (UPPER(Tipo) IN ('KING', 'QUEEN', 'DOBLE', 'SINGLE')),
    CONSTRAINT CK_CAMAS_CANTIDAD CHECK (Cantidad > 0)
);

CREATE TABLE COMODIDADES (
    HotelID NUMBER NOT NULL,
    TipoHabitacion VARCHAR2(80) NOT NULL,
    ComodidadID NUMBER NOT NULL,
    Nombre VARCHAR2(100) NOT NULL,
    CONSTRAINT PK_COMODIDADES PRIMARY KEY (HotelID, TipoHabitacion, ComodidadID),
    CONSTRAINT FK_COMOD_HAB_TIPO FOREIGN KEY (HotelID, TipoHabitacion)
        REFERENCES HABITACIONES (HotelID, Tipo)
        ON DELETE CASCADE
);

CREATE TABLE SERVICIOSHOTELTIPO (
    ServicioSolicitado NUMBER NOT NULL,
    Hotel NUMBER NOT NULL,
    TipoHabitacion VARCHAR2(80) NOT NULL,
    CONSTRAINT PK_SERVICIOSHOTELTIPO PRIMARY KEY (ServicioSolicitado, Hotel, TipoHabitacion),
    CONSTRAINT FK_SHT_SERVICIOS FOREIGN KEY (ServicioSolicitado)
        REFERENCES SERVICIOSADICIONALES (ServicioID),
    CONSTRAINT FK_SHT_HOTELES FOREIGN KEY (Hotel)
        REFERENCES HOTELES (HotelID),
    CONSTRAINT FK_SHT_HAB_TIPO FOREIGN KEY (Hotel, TipoHabitacion)
        REFERENCES HABITACIONES (HotelID, Tipo)
        ON DELETE CASCADE
);

CREATE TABLE RESERVAS (
    ReservaID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1000,
    FechaInicio DATE NOT NULL,
    FechaFin DATE NOT NULL,
    UsuarioAsociado NUMBER NOT NULL,
    HabitacionAsociada NUMBER NOT NULL,
    Estado VARCHAR2(20) DEFAULT 'activa' NOT NULL,
    NumAdultos NUMBER(3) NOT NULL,
    NumMenores3 NUMBER(3) DEFAULT 0 NOT NULL,
    CodigoConfirmacion VARCHAR2(30) NOT NULL,
    ValorTotal NUMBER(12,2) NOT NULL,
    CONSTRAINT PK_RESERVAS PRIMARY KEY (ReservaID),
    CONSTRAINT FK_RESERVAS_USUARIOS FOREIGN KEY (UsuarioAsociado)
        REFERENCES USUARIOS (UsuarioID),
    CONSTRAINT FK_RESERVAS_HABITACIONES FOREIGN KEY (HabitacionAsociada)
        REFERENCES HABITACIONES (HabitacionID),
    CONSTRAINT UK_RESERVAS_CODIGO UNIQUE (CodigoConfirmacion),
    CONSTRAINT CK_RESERVAS_FECHAS CHECK (FechaFin > FechaInicio),
    CONSTRAINT CK_RESERVAS_ESTADO CHECK (Estado IN ('activa', 'cancelada', 'finalizada')),
    CONSTRAINT CK_RESERVAS_ADULTOS CHECK (NumAdultos > 0),
    CONSTRAINT CK_RESERVAS_MENORES CHECK (NumMenores3 >= 0),
    CONSTRAINT CK_RESERVAS_VALOR CHECK (ValorTotal >= 0)
);

CREATE TABLE SERVICIOSRESERVAS (
    Reserva NUMBER NOT NULL,
    ServicioSolicitado NUMBER NOT NULL,
    CONSTRAINT PK_SERVICIOSRESERVAS PRIMARY KEY (Reserva, ServicioSolicitado),
    CONSTRAINT FK_SR_RESERVAS FOREIGN KEY (Reserva)
        REFERENCES RESERVAS (ReservaID)
        ON DELETE CASCADE,
    CONSTRAINT FK_SR_SERVICIOS FOREIGN KEY (ServicioSolicitado)
        REFERENCES SERVICIOSADICIONALES (ServicioID)
);

CREATE INDEX IX_HOTELES_CIUDAD ON HOTELES (Ciudad);
CREATE INDEX IX_HABITACIONES_HOTEL ON HABITACIONES (HotelID);
CREATE INDEX IX_RESERVAS_USUARIO ON RESERVAS (UsuarioAsociado);
CREATE INDEX IX_RESERVAS_HAB_FECHAS ON RESERVAS (HabitacionAsociada, FechaInicio, FechaFin);
CREATE INDEX IX_RESERVAS_ESTADO ON RESERVAS (Estado);
CREATE INDEX IX_SHT_HOTEL_TIPO ON SERVICIOSHOTELTIPO (Hotel, TipoHabitacion);

CREATE OR REPLACE TRIGGER TRG_RESERVAS_CLIENTE
BEFORE INSERT OR UPDATE OF UsuarioAsociado ON RESERVAS
FOR EACH ROW
DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*)
    INTO v_count
    FROM CLIENTES
    WHERE UsuarioID = :NEW.UsuarioAsociado;

    IF v_count = 0 THEN
        RAISE_APPLICATION_ERROR(-20002, 'La reserva debe estar asociada a un usuario cliente.');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_RESERVAS_DISPONIBILIDAD
BEFORE INSERT OR UPDATE OF FechaInicio, FechaFin, HabitacionAsociada, Estado ON RESERVAS
FOR EACH ROW
DECLARE
    v_count NUMBER;
BEGIN
    IF :NEW.Estado = 'activa' THEN
        SELECT COUNT(*)
        INTO v_count
        FROM RESERVAS r
        WHERE r.HabitacionAsociada = :NEW.HabitacionAsociada
          AND r.Estado = 'activa'
          AND r.ReservaID <> NVL(:NEW.ReservaID, -1)
          AND r.FechaInicio < :NEW.FechaFin
          AND r.FechaFin > :NEW.FechaInicio;

        IF v_count > 0 THEN
            RAISE_APPLICATION_ERROR(-20001, 'La habitacion no esta disponible para el rango de fechas seleccionado.');
        END IF;
    END IF;
END;
/
